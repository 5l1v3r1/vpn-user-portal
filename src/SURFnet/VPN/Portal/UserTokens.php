namespace SURFnet\VPN\Portal;

use PDO;

class UserTokens
{
    /** @var \PDO */
    private $db;

    /** @var string */
    private $prefix;

    public function __construct(PDO $db, $prefix = '')
    {
        $db->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
        $this->db = $db;
        $this->prefix = $prefix;
    }

    public function getUserAccessTokens($userId)
    {
        $stmt = $this->db->prepare(
            sprintf(
                'SELECT client_id, issued_at, scope FROM %s WHERE user_id = :user_id',
                $this->prefix.'access_token'
            )
        );
        $stmt->bindValue(':user_id', $userId, PDO::PARAM_STR);
        $stmt->execute();

        return $stmt->fetchAll(PDO::FETCH_ASSOC);
    }

    public function deleteUserAccessTokens($userId, $clientId)
    {
        $stmt = $this->db->prepare(
            sprintf(
                'DELETE FROM %s WHERE user_id = :user_id AND client_id = :client_id',
                $this->prefix.'access_token'
            )
        );
        $stmt->bindValue(':user_id', $userId, PDO::PARAM_STR);
        $stmt->bindValue(':client_id', $clientId, PDO::PARAM_STR);
        $stmt->execute();
    }
}
